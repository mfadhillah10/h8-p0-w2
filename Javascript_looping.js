// Membuat program looping

var i = 2;
var j = 20;

//Looping pertama
console.log('LOOPING PERTAMA')
while(i <= 20){
    console.log(i + ' - I love coding')
    i += 2;
}

//Looping kedua
console.log('LOOPING KEDUA')
while(j >= 1){
    console.log(j + ' - I will become a fullstack developer')
    j -= 2;
}